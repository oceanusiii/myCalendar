package com.tiendd90.model;

public class Member
{
	private String id;
	private String name;
	private String onoff;
	
	
	
	public String getId()
	{
		return id;
	}
	public void setId(String id)
	{
		this.id = id;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
	public String getOnoff()
	{
		return onoff;
	}
	public void setOnoff(String onoff)
	{
		this.onoff = onoff;
	}
}
